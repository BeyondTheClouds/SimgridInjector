package scheduling.centralized;

import scheduling.GeneralProperties;

import java.io.File;

/**
 * Created by alebre on 16/07/14.
 */
public class CentralizedResolverProperties extends GeneralProperties {

    private static final long serialVersionUID = 7229931356566105645L;

    //Default location of the properties file
    public static final String DEFAULT_PROP_FILE = "config" + File.separator + "centralizedResolver.properties";

    //Singleton
    public final static CentralizedResolverProperties INSTANCE = new CentralizedResolverProperties();


    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //Property keys
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public final static String SCHEDULING_PERIODICITY = "centralizedresolver.scheduling-periodicity";

    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //Property default values
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public final static long DEFAULT_SCHEDULING_PERIODICITY = 30;

    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //Constructors
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public CentralizedResolverProperties(String file) {
        super(file);
    }

    public CentralizedResolverProperties() {
        this(DEFAULT_PROP_FILE);
    }


    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //Class methods
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////


    public static long getSchedulingPeriodicity(){
        return INSTANCE.getPropertyAsLong(SCHEDULING_PERIODICITY, DEFAULT_SCHEDULING_PERIODICITY);
    }


    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //Methods for properties currently not stored in the properties file
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////


    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //Other methods
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////

}

